/* -->You can write double quotation inside single quotation directly.
   -->You can write double quotation inside single quotation with Escape sequence.
   --> You can not write single quotation inside single quatation directly.
   --> You can write single quotation inside single quotation with Escape sequence only. */
  

class Lab26
{
  public static void main(String args[])
  {
     char ch1='"'; //Double quotes in single quotes
     char ch2='\"';  //slash followed by Double quotes
   System.out.println(ch1);
   System.out.println(ch2);
}
}
     
  